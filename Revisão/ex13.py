# funções matemáticas
import math

# todas essas funções, são funções que já vem
# prontas no python. Outros tipos de funções
# matemática é preciso importar a biblioteca math

# valor absoluto - valor sempre positivo
print(abs(-10))

# potência - é a mesma coisa que 2 ** 3
print(pow(2, 3))

# retorna o maior valor da lista de elementos
print(max(10, 3, 1))

# retorna o menor valor da lista de elementos
print(min(10, 2, 3, 1))

# arredondar valor com duas casas decimais
print(round(3.14159), 2)

# conversão para inteiro
print(int(10.0))

# conversão para float
print(float(5))


# raiz quadrada
print(math.sqrt(4))

# fatorial
print(math.factorial(10))

# arredondar para baixo
print(math.floor(3.9))

# arredondar para cima
print(math.ceil(3.1))
